	std::string Convert::ToString(const std::wstring& val)
	{
		std::size_t size = sizeof(val.c_str());
		char *str = new char[size];
		std::string temp;
		std::wcstombs(str, val.c_str(), size);
		temp = str;
		delete[] str;
		return temp;
	}
